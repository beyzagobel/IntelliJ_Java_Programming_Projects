package kontrol_ifadeleri_07;

public class BreakMantigi4 {
    public static void main(String[] args) {
        int x=1;
        switch (x){
            case 1:
                System.out.println("1");
            case 2:
                System.out.println("2");
                break;
            default:
                System.out.println("Default");
        }
    }
}
