package donguler_08;

public class WhileDongusu {
    public static void main(String[] args) {
        /*
        - While döngüdündeki parametre, koşul doğru olduğu sürece devam eder.
        - Koşul her iterasyon sonrası kontrol edilir
        - Değişmeyen koşul kullanılırsa, sonsuz döngü olur.
         */

        int i=1;
        while(i<=100){
            System.out.println();
        }
    }
}
